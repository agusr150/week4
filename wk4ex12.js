function countProfit(shoppers) {
  let listBarang = [ ['Sepatu Stacattu', 1500000, 10],
                     ['Baju Zoro', 500000, 2],
                     ['Sweater Uniklooh', 175000, 1]
                   ];

  // you can only write your code here!
  if (shoppers.length <1){return []}
  var result=[]
  for (var j=0; j<listBarang.length; j++){
    var pembeli =[]
    var stock = listBarang[j][2]
    var obj ={}
    for (var i in shoppers){    
      if(shoppers[i].product === listBarang[j][0]){
        if(shoppers[i].amount<=stock){
          pembeli.push(shoppers[i].name)
          stock= stock-shoppers[i].amount
        }
      }
    }
    obj.product=listBarang[j][0]
    obj.shoppers = pembeli
    obj.leftOver = stock
    obj.totalProfit = (listBarang[j][2]-stock)*listBarang[j][1]
    //console.log(obj)
    result[j]=obj
  }
return result
}
  
